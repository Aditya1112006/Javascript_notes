// Arithmetic Operators
let a = 10;
let b = 5;
console.log("Addition: " + (a + b));
console.log("Subtraction: " + (a - b));
console.log("Multiplication: " + (a * b))
console.log("Division: " + (a / b));
console.log("Modulus: " + (a % b)); 
console.log("Exponentiation: " + (a ** b));

// Assignment Operators
a += 5;
console.log(a);
a -= 3;
console.log(a);
a *= 2;
console.log(a);
a /= 4;
console.log(a);
a %= 3;
console.log(a);
a **= 2;
console.log(a);

// Unary Operators
let c = 10;
console.log(++c);
console.log(c--);

// Comparison Operators
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
console.log(a == b);
console.log(a != b);
console.log(a === b);
console.log(a !== b);
